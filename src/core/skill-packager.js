/**
 * HeartFlow Skill Packager v11.21.2
 * 
 * 核心目标：传递（transmission）
 * 把知识模块打包成可发布到 Skill Market 的格式
 * 
 * 输入: knowledge-distiller.js 的输出
 * 输出: 可安装的技能包 (.hermes/skills 格式)
 * 
 * 支持市场:
 * - ClawHub (skillhub.cn)
 * - Hermes Agent Skills
 * - GitHub Raw
 */

const fs = require('fs');
const path = require('path');

const SKILL_TEMPLATE = `---
name: {name}
title: {title}
version: {version}
description: >
  {description}
  Generated by HeartFlow Knowledge Distiller v11.21.2.
  Source: {source}
  Confidence: {confidence}
tags: [{tags}]
---
{readme}
`;

// ============================================================
// 包生成器
// ============================================================

class SkillPackager {
  /**
   * @param {object} options
   * @param {string} options.outputDir - 输出目录
   */
  constructor(options = {}) {
    this.outputDir = options.outputDir || path.join(__dirname, '../../distilled/skills');
    this.skills = [];
  }

  /**
   * 打包一个 Pattern Card 为技能
   */
  packageCard(card, options = {}) {
    const {
      author = 'HeartFlow',
      tags = [],
      readmeTemplate = 'default',
    } = options;

    const skillName = sanitizeSkillName(card.title || card.id);
    const skillDir = path.join(this.outputDir, skillName);
    const version = '1.0.0';

    // 生成 README
    const readme = this._generateReadme(skillName, card, readmeTemplate);

    // 生成 SKILL.md
    const skillMd = SKILL_TEMPLATE
      .replace('{name}', skillName)
      .replace('{title}', card.title || skillName)
      .replace('{version}', version)
      .replace('{description}', (card.content || '').slice(0, 200))
      .replace('{source}', card.source || 'memory')
      .replace('{confidence}', card.confidence || '50%')
      .replace('{tags}', [card.type, ...tags, 'heartflow-generated'].filter(Boolean).join(', '))
      .replace('{readme}', readme);

    // 生成 package.json
    const packageJson = {
      name: skillName,
      version,
      description: (card.content || '').slice(0, 200),
      author,
      heartflow: {
        source: 'knowledge-distiller',
        patternId: card.id,
        confidence: card.confidence,
        generated: new Date().toISOString(),
      },
    };

    return {
      skillName,
      skillDir,
      version,
      files: {
        'SKILL.md': skillMd,
        'package.json': JSON.stringify(packageJson, null, 2),
      },
    };
  }

  /**
   * 批量打包多个卡片
   */
  packageCards(cards, options = {}) {
    const packages = [];
    for (const card of cards) {
      try {
        const pkg = this.packageCard(card, options);
        packages.push(pkg);
      } catch (e) {
        console.warn(`[SkillPackager] Failed to package ${card.id}: ${e.message}`);
      }
    }
    return packages;
  }

  /**
   * 打包知识报告为完整技能库
   */
  packageKnowledgeBase(report, options = {}) {
    const {
      author = 'HeartFlow',
      baseName = 'knowledge-base',
    } = options;

    const baseDir = path.join(this.outputDir, baseName);
    const skills = [];

    // 主 README
    const mainReadme = this._generateMainReadme(report, baseName);

    // 索引文件
    const indexFile = {
      name: baseName,
      version: '11.21.2',
      generated: new Date().toISOString(),
      patterns: report.topPatterns.map(p => ({
        id: p.id,
        title: p.title,
        type: p.type,
        confidence: p.confidence,
        actionability: p.actionability,
      })),
      stats: report.stats,
    };

    // 统计报告
    const statsReport = this._generateStatsReport(report);

    return {
      baseDir,
      files: {
        'README.md': mainReadme,
        'INDEX.json': JSON.stringify(indexFile, null, 2),
        'STATS.md': statsReport,
        'package.json': JSON.stringify({
          name: baseName,
          version: '11.21.2',
          description: 'HeartFlow distilled knowledge base',
          author,
          generated: new Date().toISOString(),
        }, null, 2),
      },
      skillCount: report.topPatterns.length,
    };
  }

  /**
   * 写入文件到磁盘
   */
  writePackage(pkg) {
    if (!fs.existsSync(pkg.skillDir || pkg.baseDir)) {
      fs.mkdirSync(pkg.skillDir || pkg.baseDir, { recursive: true });
    }

    const files = pkg.files;
    const dir = pkg.skillDir || pkg.baseDir;

    for (const [filename, content] of Object.entries(files)) {
      const filePath = path.join(dir, filename);
      fs.writeFileSync(filePath, content, 'utf-8');
    }

    return dir;
  }

  /**
   * 批量写入包
   */
  writePackages(packages) {
    const written = [];
    for (const pkg of packages) {
      try {
        const dir = this.writePackage(pkg);
        written.push(dir);
      } catch (e) {
        console.warn(`[SkillPackager] Write failed: ${e.message}`);
      }
    }
    return written;
  }

  // ============================================================
  // 内部生成函数
  // ============================================================

  _generateReadme(skillName, card, template) {
    const tpl = README_TEMPLATES[template] || README_TEMPLATES.default;

    return tpl
      .replace('{skillName}', skillName)
      .replace('{title}', card.title || skillName)
      .replace('{content}', card.content || '')
      .replace('{type}', card.type || 'general')
      .replace('{confidence}', card.confidence || '50%')
      .replace('{actionability}', card.actionability || 'REFERENCE')
      .replace('{guidance}', card.guidance || '')
      .replace('{reason}', card.reason || '')
      .replace('{tags}', (card.tags || []).join(', '))
      .replace('{age}', card.age || 'unknown')
      .replace('{generated}', new Date().toISOString());
  }

  _generateMainReadme(report, baseName) {
    return `# ${baseName}

Generated by **HeartFlow Knowledge Distiller** v11.21.2

## 统计

- **总 Pattern 数**: ${report.stats.total}
- **高置信度**: ${report.stats.highConfidence}
- **可操作**: ${report.stats.actionables}
- **近7天**: ${report.stats.recentPatterns}

## 按类型分布

${report.stats.byType.map(t => `- ${t.type}: ${t.count}`).join('\n')}

## TOP Tags

${report.stats.topTags.slice(0, 5).map(t => `- **${t.tag}**: ${t.count} patterns`).join('\n')}

## 核心 Pattern

${report.topPatterns.slice(0, 5).map(p => `### ${p.title}

- **置信度**: ${p.confidence}
- **类型**: ${p.type}
- **可操作性**: ${p.actionability}
- **内容**: ${p.content.slice(0, 100)}...
`).join('\n---\n')}

---
*Auto-generated by HeartFlow Knowledge Distiller*
`;
  }

  _generateStatsReport(report) {
    return `# Knowledge Statistics

## 概要

| 指标 | 值 |
|------|-----|
| 总 Pattern | ${report.stats.total} |
| 高置信度 | ${report.stats.highConfidence} |
| 可操作 | ${report.stats.actionables} |
| 近7天新增 | ${report.stats.recentPatterns} |

## 按层分布

| 层 | 数量 |
|----|------|
${report.stats.byLayer.map(l => `| ${l.layer} | ${l.count} |`).join('\n')}

## 按类型分布

| 类型 | 数量 |
|------|------|
${report.stats.byType.map(t => `| ${t.type} | ${t.count} |`).join('\n')}

## Top Tags

${report.stats.topTags.map(t => `- ${t.tag}: ${t.count}`).join('\n')}
`;
  }
}

